    private void unmatchedreportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unmatchedreportActionPerformed
        Vector<Transaction> unmatchedTransFromFile1 = null;
        unmatchedTransFromFile1 = ManageData.getUnmatchedOutput(transReadFromFile1);
        int row = 0;
       // TableModel dataModel = new TableModal();
        //jTable3.setModel(dataModel);

        for (Transaction unmatchedFile1 : unmatchedTransFromFile1) {

            String date = null;
            String amount = null;
            date = unmatchedFile1.getTransactionDate();
            amount = unmatchedFile1.getTransactionAmount();
            
            if(date != null)
            {
                jTable3.setValueAt(date, row, 0);
            }
            
            jTable3.setValueAt(amount, row, 1);
            row++;
        }
        this.getContentPane().add(jPanel3);
        this.pack();
    }//GEN-LAST:event_unmatchedreportActionPerformed

