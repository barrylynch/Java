    private void compareButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compareButtonActionPerformed

        int numOfTransInFile1 = 0;
        int numOfTransInFile2 = 0;
        int matchesInFile1 = 0;
        int matchesInFile2 = 0;
        
        if(transReadFromFile1 != null && transReadFromFile2 != null )
        { 
            matchesInFile1 = ManageData.matchData( transReadFromFile1, transReadFromFile2);
            numOfTransInFile1 = transReadFromFile1.size();
            
            matchesInFile2 = ManageData.matchData( transReadFromFile2, transReadFromFile1);
            numOfTransInFile2 = transReadFromFile2.size();
            
            jTextField3.setText(file1ChosenName);
            jTextField4.setText(file2ChosenName);
            
            jTable1.setValueAt("Total Records", 0, 0);
            jTable1.setValueAt("Matching Records", 1, 0);
            jTable1.setValueAt("Unmatched Records", 2, 0);
            
            jTable1.setValueAt(numOfTransInFile1, 0, 1);
            jTable1.setValueAt(matchesInFile1, 1, 1);
            jTable1.setValueAt(numOfTransInFile1 - matchesInFile1, 2, 1);
            
            jTable2.setValueAt("Total Records", 0, 0);
            jTable2.setValueAt("Matching Records", 1, 0);
            jTable2.setValueAt("Unmatched Records", 2, 0);
            
            jTable2.setValueAt(numOfTransInFile2, 0, 1);
            jTable2.setValueAt(matchesInFile2, 1, 1);
            jTable2.setValueAt(numOfTransInFile2 - matchesInFile2, 2, 1);
            getContentPane().add(jPanel2);
            remove(jPanel3);
            pack();
        }
    }//GEN-LAST:event_compareButtonActionPerformed

